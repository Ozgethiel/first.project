package main.java;

public class EvenOddNumbers {

    public static void main(String[] args) {
        int i = 2;
        int y = 1;
        for ( i = 2; i < 20; i = i + 2) {
            System.out.println(i);
        }
        for ( y = 1; y < 21; y = y + 2) {
            System.out.println(y);
        }
    }



}
